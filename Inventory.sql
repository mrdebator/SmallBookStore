DROP TABLE Inventory;
CREATE TABLE Inventory (
	Supplier_ID NUMBER PRIMARY KEY,
	BookISBN VARCHAR(10),
	QtyOnOrder NUMBER,
	OrderDate DATE,
	FOREIGN KEY (Supplier_ID) REFERENCES Suppliers(Supplier_ID),
	FOREIGN KEY (BookISBN) REFERENCES Books(BookISBN)
);

